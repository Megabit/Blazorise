namespace Blazorise.Modules;

/// <summary>
/// Represents JavaScript options specific to button interactions.
/// </summary>
public class ButtonJSOptions
{
    /// <summary>
    /// Gets or sets a value indicating whether to prevent the default behavior on submit actions.
    /// When set to <c>true</c>, the default form submission behavior will be prevented.
    /// </summary>
    public bool PreventDefaultOnSubmit { get; set; }
}