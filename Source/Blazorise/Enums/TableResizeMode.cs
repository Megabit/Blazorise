namespace Blazorise
{
    /// <summary>
    /// Defines the resize mode of the Table's columns.
    /// </summary>
    public enum TableResizeMode
    {
        /// <summary>
        /// The Table can only be resized from the columns header.
        /// </summary>
        Header,

        /// <summary>
        /// The Table can be resized from the entire column area.
        /// </summary>
        Columns
    }
}
