namespace Blazorise;

/// <summary>
/// Changes the size of the modal.
/// </summary>
public enum ModalSize
{
    /// <summary>
    /// Default modal size.
    /// </summary>
    Default,

    /// <summary>
    /// Small modal.
    /// </summary>
    Small,

    /// <summary>
    /// Large modal.
    /// </summary>
    Large,

    /// <summary>
    /// Extra large modal.
    /// </summary>
    ExtraLarge,

    /// <summary>
    /// Defines the modal that covers the user viewport.
    /// </summary>
    Fullscreen,
}