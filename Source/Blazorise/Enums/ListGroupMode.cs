namespace Blazorise;

/// <summary>
/// Defines the <see cref="ListGroup"/> behavior.
/// </summary>
public enum ListGroupMode
{
    /// <summary>
    /// List group will act as a static list.
    /// </summary>
    Static,

    /// <summary>
    /// List group will act on clicking the items.
    /// </summary>
    Selectable,
}