#region Using directives
#endregion

namespace Blazorise
{
    /// <summary>
    /// Defines the alignment of an element.
    /// </summary>
    public enum Alignment
    {
        /// <summary>
        /// No alignment will be applied.
        /// </summary>
        None,

        /// <summary>
        /// Aligns an element to the left.
        /// </summary>
        Start,

        /// <summary>
        /// Aligns an element on the center.
        /// </summary>
        Center,

        /// <summary>
        /// Aligns an element to the right.
        /// </summary>
        End,
    }
}
