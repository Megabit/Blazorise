namespace Blazorise
{
    /// <summary>
    /// The flex-grow property sets the flex grow factor to the provided <see cref="FlexGrowShrinkSize">number</see>.
    /// </summary>
    public enum FlexGrowShrink
    {
        /// <summary>
        /// No grow or shrink will be applied.
        /// </summary>
        None,

        /// <summary>
        /// Grow rule will be applied.
        /// </summary>
        Grow,

        /// <summary>
        /// Shrink rule will be applied.
        /// </summary>
        Shrink,
    }
}
