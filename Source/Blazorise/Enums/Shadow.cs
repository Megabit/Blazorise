namespace Blazorise
{
    /// <summary>
    /// Add or remove shadows to elements with box-shadow utilities.
    /// </summary>
    public enum Shadow
    {
        /// <summary>
        /// Shadow will not be applied.
        /// </summary>
        None,

        /// <summary>
        /// Disables any shadow on the element.
        /// </summary>
        Remove,

        /// <summary>
        /// Regular shadow.
        /// </summary>
        Default,

        /// <summary>
        /// Small shadow.
        /// </summary>
        Small,

        /// <summary>
        /// Larger shadow.
        /// </summary>
        Large,
    }
}
