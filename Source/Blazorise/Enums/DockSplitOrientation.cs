namespace Blazorise;

/// <summary>
/// Defines the orientation of a dock split.
/// </summary>
public enum DockSplitOrientation
{
    /// <summary>
    /// Places the first and second dock nodes side by side.
    /// </summary>
    Horizontal,

    /// <summary>
    /// Places the first dock node above the second dock node.
    /// </summary>
    Vertical,
}