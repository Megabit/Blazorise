namespace Blazorise
{
    /// <summary>
    /// Defines the text weight.
    /// </summary>
    public enum TextWeight
    {
        /// <summary>
        /// No weight will be applied.
        /// </summary>
        Default,

        /// <summary>
        /// Defines normal characters. This is default.
        /// </summary>
        Normal,

        /// <summary>
        /// Defines thick characters.
        /// </summary>
        Bold,

        /// <summary>
        /// Defines lighter characters.
        /// </summary>
        Light,
    }
}
