namespace Blazorise;

/// <summary>
/// Defines the layout used by the on-screen keyboard.
/// </summary>
public enum OnScreenKeyboardLayout
{
    /// <summary>
    /// A general text keyboard layout.
    /// </summary>
    Text,

    /// <summary>
    /// A numeric keyboard layout.
    /// </summary>
    Numeric,

    /// <summary>
    /// A decimal keyboard layout.
    /// </summary>
    Decimal,

    /// <summary>
    /// An email keyboard layout.
    /// </summary>
    Email,

    /// <summary>
    /// A URL keyboard layout.
    /// </summary>
    Url,

    /// <summary>
    /// A telephone keyboard layout.
    /// </summary>
    Telephone,
}