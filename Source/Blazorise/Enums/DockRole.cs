namespace Blazorise;

/// <summary>
/// Defines the logical role of a dock pane.
/// </summary>
public enum DockRole
{
    /// <summary>
    /// Represents a regular tool pane that is usually docked around the document area.
    /// </summary>
    Tool,

    /// <summary>
    /// Represents a document pane that is usually hosted in the center document area.
    /// </summary>
    Document,
}