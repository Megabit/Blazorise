namespace Blazorise;

/// <summary>
/// Specifies the buttons on a mouse device.
/// </summary>
/// <remarks>This enumeration is used to identify mouse buttons in input handling scenarios, such as detecting
/// which button was pressed during a mouse event. The values correspond to the left, middle, and right buttons on a
/// standard mouse.</remarks>
public enum MouseButton
{
    /// <summary>
    /// The left mouse button.
    /// </summary>
    Left = 0,

    /// <summary>
    /// The middle mouse button.
    /// </summary>
    Middle = 1,

    /// <summary>
    /// The right mouse button.
    /// </summary>
    Right = 2,
}