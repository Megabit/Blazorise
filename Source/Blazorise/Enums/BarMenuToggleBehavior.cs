namespace Blazorise;

/// <summary>
/// Defines how the bar menu will behave when toggled.
/// </summary>
public enum BarMenuToggleBehavior
{
    /// <summary>
    /// Only a single menu will be toggled at a time.
    /// </summary>
    AllowSingleMenu,

    /// <summary>
    /// Multiple menus can be toggled at a time.
    /// </summary>
    AllowMultipleMenus
}