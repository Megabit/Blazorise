namespace Blazorise;

/// <summary>
/// Specifies the position of a table caption relative to the table.
/// </summary>
public enum TableCaptionSide
{
    /// <summary>
    /// No side will be defined.
    /// </summary>
    Default,

    /// <summary>
    /// Places the caption at the top of the table.
    /// </summary>
    Top,

    /// <summary>
    /// Places the caption at the bottom of the table.
    /// </summary>
    Bottom,
}
