namespace Blazorise;

/// <summary>
/// Defines the number of rows to show in a grid.
/// </summary>
public enum GridRowsSize
{
    /// <summary>
    /// Nothing will be applied.
    /// </summary>
    Default,

    /// <summary>
    /// One row per grid.
    /// </summary>
    Are1,

    /// <summary>
    /// Two rows per grid.
    /// </summary>
    Are2,

    /// <summary>
    /// Three rows per grid.
    /// </summary>
    Are3,

    /// <summary>
    /// Four rows per grid.
    /// </summary>
    Are4,

    /// <summary>
    /// Five rows per grid.
    /// </summary>
    Are5,

    /// <summary>
    /// Six rows per grid.
    /// </summary>
    Are6,
}