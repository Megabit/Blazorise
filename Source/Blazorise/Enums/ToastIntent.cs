namespace Blazorise;

/// <summary>
/// Defines the intent of a toast message.
/// </summary>
public enum ToastIntent
{
    /// <summary>
    /// Shows an info message.
    /// </summary>
    Info,

    /// <summary>
    /// Shows a success message.
    /// </summary>
    Success,

    /// <summary>
    /// Shows a warning message.
    /// </summary>
    Warning,

    /// <summary>
    /// Shows an error message.
    /// </summary>
    Error,
}