namespace Blazorise;

/// <summary>
/// Defines the visual width of the on-screen keyboard.
/// </summary>
public enum OnScreenKeyboardSize
{
    /// <summary>
    /// The keyboard uses the full available width.
    /// </summary>
    FullWidth,

    /// <summary>
    /// The keyboard uses a small centered width.
    /// </summary>
    Small,

    /// <summary>
    /// The keyboard uses a medium centered width.
    /// </summary>
    Medium,

    /// <summary>
    /// The keyboard uses a large centered width.
    /// </summary>
    Large,
}