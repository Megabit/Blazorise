namespace Blazorise
{
    /// <summary>
    /// Defines the border sizes.
    /// </summary>
    public enum BorderSize
    {
        /// <summary>
        /// Border size will not be applied.
        /// </summary>
        None,

        /// <summary>
        /// Makes the element borderless.
        /// </summary>
        Is0,

        /// <summary>
        /// Borders will be 1px wide.
        /// </summary>
        Is1,

        /// <summary>
        /// Borders will be 2px wide.
        /// </summary>
        Is2,

        /// <summary>
        /// Borders will be 3px wide.
        /// </summary>
        Is3,

        /// <summary>
        /// Borders will be 4px wide.
        /// </summary>
        Is4,

        /// <summary>
        /// Borders will be 5px wide.
        /// </summary>
        Is5,
    }
}
