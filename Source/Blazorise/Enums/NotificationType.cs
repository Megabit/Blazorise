namespace Blazorise
{
    /// <summary>
    /// Defines the possible UI notification types with predefined actions.
    /// </summary>
    public enum NotificationType
    {
        /// <summary>
        /// Shows the simple info message.
        /// </summary>
        Info,

        /// <summary>
        /// Shows the success message.
        /// </summary>
        Success,

        /// <summary>
        /// Shows the warning message.
        /// </summary>
        Warning,

        /// <summary>
        /// Shows the error message.
        /// </summary>
        Error,
    }
}
