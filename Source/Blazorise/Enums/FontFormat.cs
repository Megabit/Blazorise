namespace Blazorise;

/// <summary>
/// Defines the source format for registered Blazorise fonts.
/// </summary>
public enum FontFormat
{
    /// <summary>
    /// TrueType font.
    /// </summary>
    TrueType,

    /// <summary>
    /// OpenType font.
    /// </summary>
    OpenType,

    /// <summary>
    /// Web Open Font Format.
    /// </summary>
    Woff,

    /// <summary>
    /// Web Open Font Format 2.
    /// </summary>
    Woff2,
}