namespace Blazorise;

/// <summary>
/// Defines where tabs are displayed inside a dock pane group.
/// </summary>
public enum DockPaneTabPosition
{
    /// <summary>
    /// Uses the default dock layout behavior.
    /// </summary>
    Default,

    /// <summary>
    /// Displays tabs above the pane content.
    /// </summary>
    Top,

    /// <summary>
    /// Displays tabs below the pane content.
    /// </summary>
    Bottom,
}