namespace Blazorise;

/// <summary>
/// Defines the different types of animations that can be applied to a skeleton.
/// </summary>
public enum SkeletonAnimation
{
    /// <summary>
    /// The default animation with no special effects.
    /// </summary>
    Default,

    /// <summary>
    /// An animation that makes the skeleton wave.
    /// </summary>
    Wave,

    /// <summary>
    /// An animation that creates a pulsing effect.
    /// </summary>
    Pulse,
}