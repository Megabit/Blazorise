namespace Blazorise;

/// <summary>
/// Defines the sizes for the flex-basis property.
/// </summary>
public enum FlexBasisSize
{
    /// <summary>
    /// The default value. The browser calculates the flex-basis property.
    /// </summary>
    Default,

    /// <summary>
    /// The flex-basis property is set to 0.
    /// </summary>
    Is0,

    /// <summary>
    /// The flex-basis property is set to auto.
    /// </summary>
    Auto,

    /// <summary>
    /// The flex-basis property is set to 100%.
    /// </summary>
    Full,
}