        /// <summary>
        /// Primary color.
        /// </summary>
        Primary,

        /// <summary>
        /// Secondary color.
        /// </summary>
        Secondary,

        /// <summary>
        /// Success color.
        /// </summary>
        Success,

        /// <summary>
        /// Danger color.
        /// </summary>
        Danger,

        /// <summary>
        /// Warning color.
        /// </summary>
        Warning,

        /// <summary>
        /// Info color.
        /// </summary>
        Info,

        /// <summary>
        /// Light color.
        /// </summary>
        Light,

        /// <summary>
        /// Dark color.
        /// </summary>
        Dark,

        /// <summary>
        /// Body color.
        /// </summary>
        Body,

        /// <summary>
        /// Muted color.
        /// </summary>
        Muted,

        /// <summary>
        /// White color.
        /// </summary>
        White,

        /// <summary>
        /// Black text with 50% opacity on white background.
        /// </summary>
        Black50,

        /// <summary>
        /// White text with 50% opacity on black background.
        /// </summary>
        White50,
    }
