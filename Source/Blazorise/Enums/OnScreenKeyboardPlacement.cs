namespace Blazorise;

/// <summary>
/// Defines where the on-screen keyboard is rendered.
/// </summary>
public enum OnScreenKeyboardPlacement
{
    /// <summary>
    /// The keyboard is fixed to the bottom of the viewport.
    /// </summary>
    Bottom,

    /// <summary>
    /// The keyboard is fixed to the top of the viewport.
    /// </summary>
    Top,

    /// <summary>
    /// The keyboard is rendered inline.
    /// </summary>
    Inline,
}