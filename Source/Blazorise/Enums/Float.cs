#region Using directives
#endregion

namespace Blazorise
{
    /// <summary>
    /// Floats an element to the left or right, or disable floating.
    /// </summary>
    public enum Float
    {
        /// <summary>
        /// Don't float on all viewport sizes.
        /// </summary>
        None,

        /// <summary>
        /// Float left on all viewport sizes.
        /// </summary>
        Left,

        /// <summary>
        /// Float right on all viewport sizes.
        /// </summary>
        Right,
    }
}
