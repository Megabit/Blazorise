namespace Blazorise
{
    /// <summary>
    /// Specifies the direction of a sort operation.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// No sorting will be applied.
        /// </summary>
        Default = 0,

        /// <summary>
        /// Sorts in ascending order.
        /// </summary>
        Ascending = 1,

        /// <summary>
        /// Sorts in descending order.
        /// </summary>
        Descending = 2,
    }
}
