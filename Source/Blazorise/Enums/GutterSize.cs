namespace Blazorise;

/// <summary>
/// Utilities for controlling padding between your columns.
/// </summary>
public enum GutterSize
{
    /// <summary>
    /// For classes that eliminate the padding by setting it to 0.
    /// </summary>
    Is0,

    /// <summary>
    /// (by default) for classes that set the padding to size 1.
    /// </summary>
    Is1,

    /// <summary>
    /// (by default) for classes that set the padding to size 2.
    /// </summary>
    Is2,

    /// <summary>
    /// (by default) for classes that set the padding to size 3.
    /// </summary>
    Is3,

    /// <summary>
    /// (by default) for classes that set the padding to size 4.
    /// </summary>
    Is4,

    /// <summary>
    /// (by default) for classes that set the padding to size 5.
    /// </summary>
    Is5,
}