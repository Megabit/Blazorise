namespace Blazorise;

/// <summary>
/// Defines the responsive mode of the Table.
/// </summary>
public enum TableResponsiveMode
{
    /// <summary>
    /// The default Table responsive mode.
    /// </summary>
    Default,

    /// <summary>
    /// The table will have a dedicated layout for mobile devices where the columns are stacked on top of each other.
    /// <para>NOTE: In this mode the table may have limited functionality</para>
    /// </summary>
    Mobile
}