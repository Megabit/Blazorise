namespace Blazorise
{
    /// <summary>
    /// Defines the mode in which the dates can be selected.
    /// </summary>
    public enum DateInputSelectionMode
    {
        /// <summary>
        /// Only one date can be selected.
        /// </summary>
        Single,

        /// <summary>
        /// Allowed to select a range of dates.
        /// </summary>
        Range,

        /// <summary>
        /// Allowed to select multiple dates.
        /// </summary>
        Multiple,
    }
}
