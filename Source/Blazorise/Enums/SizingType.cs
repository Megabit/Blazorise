namespace Blazorise
{
    /// <summary>
    /// Defines the sizing types.
    /// </summary>
    public enum SizingType
    {
        /// <summary>
        /// No sizing will be defined.
        /// </summary>
        None,

        /// <summary>
        /// Sizing will be defined for the element width attribute(s).
        /// </summary>
        Width,

        /// <summary>
        /// Sizing will be defined for the element height attribute(s).
        /// </summary>
        Height,
    }
}
