namespace Blazorise
{
    /// <summary>
    /// Defines the type of position edge type.
    /// </summary>
    public enum PositionEdgeType
    {
        /// <summary>
        /// Edge type will not be applied.
        /// </summary>
        None,

        /// <summary>
        /// For the vertical top position.
        /// </summary>
        Top,

        /// <summary>
        /// For the horizontal left position.
        /// </summary>
        Left,

        /// <summary>
        /// For the vertical bottom position.
        /// </summary>
        Bottom,

        /// <summary>
        /// For the horizontal right position.
        /// </summary>
        Right,
    }
}
