namespace Blazorise
{
    /// <summary>
    /// Specifies horizontal line style variants.
    /// </summary>
    public enum DividerType
    {
        /// <summary>
        /// Horizontal line will be solid.
        /// </summary>
        Solid,

        /// <summary>
        /// Horizontal line will be dashed.
        /// </summary>
        Dashed,

        /// <summary>
        /// Horizontal line will be dotted.
        /// </summary>
        Dotted,

        /// <summary>
        /// Horizontal line be separated by text.
        /// </summary>
        TextContent,
    }
}
