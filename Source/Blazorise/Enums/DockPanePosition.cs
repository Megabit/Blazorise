namespace Blazorise;

/// <summary>
/// Defines the position where a <see cref="DockPane"/> is placed inside a <see cref="DockLayout"/>.
/// </summary>
public enum DockPanePosition
{
    /// <summary>
    /// Places the pane on the left side of the layout.
    /// </summary>
    Left,

    /// <summary>
    /// Places the pane on the right side of the layout.
    /// </summary>
    Right,

    /// <summary>
    /// Places the pane at the top of the layout.
    /// </summary>
    Top,

    /// <summary>
    /// Places the pane at the bottom of the layout.
    /// </summary>
    Bottom,

    /// <summary>
    /// Places the pane in the center document area of the layout.
    /// </summary>
    Center,
}