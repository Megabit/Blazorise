namespace Blazorise;

/// <summary>
/// Defines the position where a <see cref="DockPanel"/> is placed inside a <see cref="DockLayout"/>.
/// </summary>
public enum DockPanelPosition
{
    /// <summary>
    /// Places the panel on the left side of the layout.
    /// </summary>
    Left,

    /// <summary>
    /// Places the panel on the right side of the layout.
    /// </summary>
    Right,

    /// <summary>
    /// Places the panel at the top of the layout.
    /// </summary>
    Top,

    /// <summary>
    /// Places the panel at the bottom of the layout.
    /// </summary>
    Bottom,
}