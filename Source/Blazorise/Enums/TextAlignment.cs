using System;

namespace Blazorise
{
    /// <summary>
    /// Defines the text alignment.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// No alignment will be applied.
        /// </summary>
        Default,

        /// <summary>
        /// Aligns the text to the start.
        /// </summary>
        Start,

        /// <summary>
        /// Aligns the text to the end.
        /// </summary>
        End,

        /// <summary>
        /// Centers the text.
        /// </summary>
        Center,

        /// <summary>
        /// Stretches the lines so that each line has equal width.
        /// </summary>
        Justified
    }
}
