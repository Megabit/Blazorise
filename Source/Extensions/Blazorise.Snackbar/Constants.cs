namespace Blazorise.Snackbar
{
    /// <summary>
    /// Defines the snackbar constant values.
    /// </summary>
    public static class Constants
    {
        /// <summary>
        /// Defines the default interval(in milliseconds) after which the snackbar will be automatically closed.
        /// </summary>
        public const double DefaultIntervalBeforeClose = 5000;

        /// <summary>
        /// Defines the default interval(in milliseconds) by which the snackbar will be delayed from closing.
        /// </summary>
        public const double DefaultOnClickIntervalBeforeClose = 10000;
    }
}
