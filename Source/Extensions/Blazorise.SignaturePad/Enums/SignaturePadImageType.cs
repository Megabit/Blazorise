namespace Blazorise.SignaturePad;

/// <summary>
/// Defines an image type that is processed by the <see cref="SignaturePad"/> component.
/// </summary>
public enum SignaturePadImageType
{
    /// <summary>
    /// Process an image in png format.
    /// </summary>
    Png,

    /// <summary>
    /// Process an image in jpeg format.
    /// </summary>
    Jpeg,

    /// <summary>
    /// Process an image in svg format.
    /// </summary>
    Svg,
}