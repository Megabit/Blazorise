namespace Blazorise.PdfViewer;

/// <summary>
/// Specifies the orientation of a PDF document.
/// </summary>
public enum PdfOrientation
{
    /// <summary>
    /// The PDF document is in portrait orientation (taller than it is wide).
    /// </summary>
    Portrait,

    /// <summary>
    /// The PDF document is in landscape orientation (wider than it is tall).
    /// </summary>
    Landscape,
}