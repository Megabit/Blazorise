namespace Blazorise.Scheduler;

/// <summary>
/// Specifies the week of the month for a scheduler event.
/// </summary>
public enum SchedulerWeek
{
    /// <summary>
    /// Represents the first week of the month.
    /// </summary>
    First = 1,

    /// <summary>
    /// Represents the second week of the month.
    /// </summary>
    Second = 2,

    /// <summary>
    /// Represents the third week of the month.
    /// </summary>
    Third = 3,

    /// <summary>
    /// Represents the fourth week of the month.
    /// </summary>
    Fourth = 4,

    /// <summary>
    /// Represents the last week of the month.
    /// </summary>
    Last = -1
}