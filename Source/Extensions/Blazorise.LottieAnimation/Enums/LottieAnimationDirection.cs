namespace Blazorise.LottieAnimation;

/// <summary>
/// Direction of the lottie animation.
/// </summary>
public enum LottieAnimationDirection
{
    /// <summary>
    /// Animation will play forwards.
    /// </summary>
    Forward = 1,

    /// <summary>
    /// Animation will play backwards.
    /// </summary>
    Backward = -1,
}