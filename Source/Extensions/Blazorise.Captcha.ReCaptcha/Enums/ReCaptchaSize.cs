namespace Blazorise.Captcha.ReCaptcha;

/// <summary>
/// The size of the widget.
/// </summary>
public enum ReCaptchaSize
{
    /// <summary>
    /// The normal size of the widget.
    /// </summary>
    Normal,

    /// <summary>
    /// The compact size of the widget.
    /// </summary>
    Compact,

    /// <summary>
    /// Used to create an invisible widget bound to a div and programmatically executed.
    /// </summary>
    Invisible,
}
