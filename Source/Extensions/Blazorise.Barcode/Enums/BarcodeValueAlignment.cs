namespace Blazorise.Barcode;

/// <summary>
/// Defines the alignment of the human-readable barcode value.
/// </summary>
public enum BarcodeValueAlignment
{
    /// <summary>
    /// Aligns the value text to the start.
    /// </summary>
    Start,

    /// <summary>
    /// Aligns the value text to the center.
    /// </summary>
    Center,

    /// <summary>
    /// Aligns the value text to the end.
    /// </summary>
    End
}