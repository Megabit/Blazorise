namespace Blazorise.Video;

/// <summary>
/// Defines the video control types.
/// </summary>
public static class VideoControlsType
{
    /// <summary>
    /// The large play button in the center.
    /// </summary>
    public const string PlayLarge = "play-large";

    /// <summary>
    /// Restart playback.
    /// </summary>
    public const string Restart = "restart";

    /// <summary>
    /// Rewind by the seek time (default 10 seconds).
    /// </summary>
    public const string Rewind = "rewind";

    /// <summary>
    /// Play/pause playback.
    /// </summary>
    public const string Play = "play";

    /// <summary>
    /// Fast forward by the seek time (default 10 seconds).
    /// </summary>
    public const string FastForward = "fast-forward";

    /// <summary>
    /// The progress bar and scrubber for playback and buffering
    /// </summary>
    public const string Progress = "progress";

    /// <summary>
    /// The current time of playback.
    /// </summary>
    public const string CurrentTime = "current-time";

    /// <summary>
    /// The full duration of the media.
    /// </summary>
    public const string Duration = "duration";

    /// <summary>
    /// Toggle mute.
    /// </summary>
    public const string Mute = "mute";

    /// <summary>
    /// Volume control.
    /// </summary>
    public const string Volume = "volume";

    /// <summary>
    /// Toggle captions.
    /// </summary>
    public const string Captions = "captions";

    /// <summary>
    /// Settings menu.
    /// </summary>
    public const string Settings = "settings";

    /// <summary>
    /// Picture-in-picture (currently Safari only).
    /// </summary>
    public const string Pip = "pip";

    /// <summary>
    /// Airplay (currently Safari only).
    /// </summary>
    public const string Airplay = "airplay";

    /// <summary>
    /// Show a download button with a link to either the current source or a custom URL you specify in your options.
    /// </summary>
    public const string Download = "download";

    /// <summary>
    /// Toggle fullscreen.
    /// </summary>
    public const string Fullscreen = "fullscreen";
}