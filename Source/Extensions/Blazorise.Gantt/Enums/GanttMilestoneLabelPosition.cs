namespace Blazorise.Gantt;

/// <summary>
/// Defines where a Gantt milestone label is rendered.
/// </summary>
public enum GanttMilestoneLabelPosition
{
    /// <summary>
    /// The label is not rendered.
    /// </summary>
    None,

    /// <summary>
    /// The label is rendered at the top of the timeline body.
    /// </summary>
    Top,

    /// <summary>
    /// The label is rendered at the bottom of the timeline body.
    /// </summary>
    Bottom,
}