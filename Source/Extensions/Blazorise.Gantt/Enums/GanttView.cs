namespace Blazorise.Gantt;

/// <summary>
/// Represents the view mode of the Gantt chart.
/// </summary>
public enum GanttView
{
    /// <summary>
    /// Shows a single day timeline.
    /// </summary>
    Day,

    /// <summary>
    /// Shows a single week timeline.
    /// </summary>
    Week,

    /// <summary>
    /// Shows a single month timeline.
    /// </summary>
    Month,

    /// <summary>
    /// Shows a single year timeline.
    /// </summary>
    Year,
}