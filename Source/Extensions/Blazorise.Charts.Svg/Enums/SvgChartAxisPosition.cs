namespace Blazorise.Charts.Svg;

/// <summary>
/// Defines the native SVG chart axis position.
/// </summary>
public enum SvgChartAxisPosition
{
    #region Values

    /// <summary>
    /// Axis position is selected by the chart type.
    /// </summary>
    Auto,

    /// <summary>
    /// Axis is positioned on the left side of the chart.
    /// </summary>
    Left,

    /// <summary>
    /// Axis is positioned on the right side of the chart.
    /// </summary>
    Right,

    /// <summary>
    /// Axis is positioned on the top side of the chart.
    /// </summary>
    Top,

    /// <summary>
    /// Axis is positioned on the bottom side of the chart.
    /// </summary>
    Bottom

    #endregion
}