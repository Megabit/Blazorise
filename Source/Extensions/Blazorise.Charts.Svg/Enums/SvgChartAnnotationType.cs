namespace Blazorise.Charts.Svg;

/// <summary>
/// Defines the native SVG chart annotation type.
/// </summary>
public enum SvgChartAnnotationType
{
    /// <summary>
    /// Renders a line annotation.
    /// </summary>
    Line,

    /// <summary>
    /// Renders a box annotation.
    /// </summary>
    Box,

    /// <summary>
    /// Renders a label annotation.
    /// </summary>
    Label,

    /// <summary>
    /// Renders a point annotation.
    /// </summary>
    Point,

    /// <summary>
    /// Renders an ellipse annotation.
    /// </summary>
    Ellipse
}