namespace Blazorise.Charts.Svg;

/// <summary>
/// Defines the legend position in an SVG chart.
/// </summary>
public enum SvgChartLegendPosition
{
    #region Values

    /// <summary>
    /// Do not render the legend.
    /// </summary>
    None,

    /// <summary>
    /// Render the legend above the chart plot area.
    /// </summary>
    Top,

    /// <summary>
    /// Render the legend below the chart plot area.
    /// </summary>
    Bottom

    #endregion
}