/// <summary>
/// Defines the native SVG chart annotation label position.
/// </summary>
public enum SvgChartAnnotationLabelPosition
{
    /// <summary>
    /// Positions the label at the start of the annotation.
    /// </summary>
    Start,

    /// <summary>
    /// Positions the label at the annotation center.
    /// </summary>
    Center,

    /// <summary>
    /// Positions the label at the end of the annotation.
    /// </summary>
    End
}