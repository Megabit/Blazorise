namespace Blazorise.Charts.Svg;

/// <summary>
/// Defines logical spacing for a native SVG chart option.
/// </summary>
public class SvgChartSpacing
{
    #region Properties

    /// <summary>
    /// Defines the top spacing.
    /// </summary>
    public double Top { get; set; }

    /// <summary>
    /// Defines the end spacing.
    /// </summary>
    public double End { get; set; }

    /// <summary>
    /// Defines the bottom spacing.
    /// </summary>
    public double Bottom { get; set; }

    /// <summary>
    /// Defines the start spacing.
    /// </summary>
    public double Start { get; set; }

    #endregion
}