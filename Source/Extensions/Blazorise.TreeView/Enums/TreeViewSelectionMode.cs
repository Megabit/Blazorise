namespace Blazorise.TreeView;

/// <summary>
/// Defines the selection mode of the treeview.
/// </summary>
public enum TreeViewSelectionMode
{
    /// <summary>
    /// The treeview can only select one node at a time.
    /// </summary>
    Single,

    /// <summary>
    /// The treeview can select multiple nodes with checkbox support.
    /// </summary>
    Multiple
}
